#Perímetro del cuadrado
lado_cuadrado = 5
perimetro_cuadrado = 4 * lado_cuadrado
print(f"Perímetro del cuadrado con lado {lado_cuadrado}: {perimetro_cuadrado}")

#Perímetro del rectángulo
lado_rectangulo1 = 4
lado_rectangulo2 = 6
perimetro_rectangulo = 2 * (lado_rectangulo1 + lado_rectangulo2)
print(f"Perímetro del rectángulo con lados {lado_rectangulo1} y {lado_rectangulo2}: {perimetro_rectangulo}")

#Perímrto del triángulo
lado_triangulo1 = 3
lado_triangulo2 = 4
lado_triangulo3 = 5
perimetro_triangulo = lado_triangulo1 + lado_triangulo2 + lado_triangulo3
print(f"Perímetro del triángulo con lados {lado_triangulo1}, {lado_triangulo2} y {lado_triangulo3}: {perimetro_triangulo}")

#Perímetro del rombo
lado_rombo = 7
perimetro_rombo = 4 * lado_rombo
print(f"Perímetro del rombo con lado {lado_rombo}: {perimetro_rombo}")